// Arithmetic Operator
let a =5;
let b = 2;
console.log("a = ",a," & b = ",b);
console.log("a + b = ",a+b);
console.log("a - b = ",a-b);
console.log("a * b = ",a*b);
console.log("a / b = ",a/b);
console.log("a % b = ",a%b);
// for exponential we use ** 
console.log("a^b = ",a**b);
console.log("a = ",++a);
console.log("a = ",a++);
console.log("a = ",a--);
console.log("a = ",--a);

a+=4;
console.log("a = ",a);